// Poll & Vote Slideshow Modal Logic
function setupPollSlideshowModal() {
    const modal = document.getElementById('poll-slideshow-modal');
    if (!modal) return;
    const slides = modal.querySelectorAll('.slide');
    const skipBtn = modal.querySelector('.skip-modal');
    const getStartedBtn = modal.querySelector('.get-started');
    const modalContent = modal.querySelector('.modal-content');
    let current = 0;
    let isTransitioning = false;

    function showSlide(idx) {
        slides.forEach((slide, i) => {
            slide.classList.remove('active', 'fade-out');
            if (i === idx) {
                slide.classList.add('active');
            }
        });
        current = idx;
        isTransitioning = false;
    }

    // Event delegation for Next button with fade-out logic
    if (modalContent) {
        modalContent.addEventListener('click', function(e) {
            if (e.target.classList.contains('next-slide') && !isTransitioning) {
                isTransitioning = true;
                const currentSlide = slides[current];
                currentSlide.classList.add('fade-out');
                currentSlide.classList.remove('active');
                currentSlide.addEventListener('transitionend', function handler() {
                    currentSlide.removeEventListener('transitionend', handler);
                    showSlide(current + 1);
                });
            }
        });
    }

    function closeModal() {
        modal.style.display = 'none';
    }

    if (skipBtn) skipBtn.onclick = closeModal;
    if (getStartedBtn) getStartedBtn.onclick = closeModal;

    // Show first slide on load
    showSlide(0);
}

document.addEventListener('DOMContentLoaded', setupPollSlideshowModal);

// Poll & Vote Poll Submission Logic
function setupPollSubmitForm() {
    const form = document.querySelector('.poll-submit-form');
    if (!form) return;
    const textarea = form.querySelector('textarea');

    // Auto-capitalize first letter as user types
    textarea.addEventListener('input', function(e) {
        if (this.value.length === 1) {
            this.value = this.value.toUpperCase();
        } else if (this.value.length > 1 && this.value[0] !== this.value[0].toUpperCase()) {
            this.value = this.value[0].toUpperCase() + this.value.slice(1);
        }
    });

    form.addEventListener('submit', function(e) {
        e.preventDefault();
        const value = textarea.value.trim();
        // Require at least one word (letters)
        if (!value || !/\w+/.test(value)) {
            alert('Please enter a valid poll question.');
            textarea.focus();
            return;
        }
        // Optionally, show a confirmation (replace with your logic)
        alert('Poll submitted: ' + value);
        textarea.value = '';
    });
}

document.addEventListener('DOMContentLoaded', setupPollSubmitForm); 